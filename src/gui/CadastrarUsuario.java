    public CadastrarUsuario() {
        initComponents();
        for (int i = 0; i < 31; i++) {
            dia.insertItemAt(""+(i+1),i);
        }
        for (int i = 0; i < 12; i++) {
            mes.insertItemAt(""+(i+1),i);
        }
        for (int i = 0; i < 33; i++) {
            ano.insertItemAt(""+(i+1990),i);
        }
        confirmarCadastroUsuario.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                try {
                    Viajante viajante = new Viajante(nome.getText(), new Data(Integer.parseInt((String)dia.getSelectedItem()),Integer.parseInt((String)mes.getSelectedItem()), Integer.parseInt((String)ano.getSelectedItem())), cpf.getText(), new Senha(senha.getText()));
                    try {
                        Fachada.getInstance().cadastrarViajante(viajante);
                        mensagem("O usuario foi cadastrado com sucesso!", 1);
                        login.setVisible(true);
                        setVisible(false);
                    } catch (Exception ex) {
                        mensagem("O usuario já está cadastrado", 0);
                    }
                } catch (SenhaInvalidaException ex) {
                    mensagem("A senha está invalida", 0);
                }
            }
        });
        buttonCancelar.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e){
                login.setVisible(true);
                setVisible(false);
            }
        });
    }
    
    private void mensagem(String mensagem, int tipo){
        /*
        Erro: ERROR_MESSAGE = 0
        Informação: INFORMATION_MESSAGE = 1
        Advertência: WARNING_MESSAGE = 2
        Pergunta: QUESTION_MESSAGE = 3
        Plana: PLAIN_MESSAGE = 4
        */
        switch(tipo){
            case 0 :
                JOptionPane.showMessageDialog(this, mensagem, "Mensagem", JOptionPane.ERROR_MESSAGE);
            break;
            case 1 :
                JOptionPane.showMessageDialog(this, mensagem, "Mensagem", JOptionPane.INFORMATION_MESSAGE);
            break;
            case 2 :
                JOptionPane.showMessageDialog(this, mensagem, "Mensagem", JOptionPane.WARNING_MESSAGE);
            break;
            case 3 :
                JOptionPane.showMessageDialog(this, mensagem, "Mensagem", JOptionPane.QUESTION_MESSAGE);
            break;
            case 4 :
                JOptionPane.showMessageDialog(this, mensagem, "Mensagem", JOptionPane.PLAIN_MESSAGE);
            break;
        }
    }
